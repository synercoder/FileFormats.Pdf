namespace Synercoding.FileFormats.Pdf;

/// <summary>
/// Enum representing the rotations a page can have
/// </summary>
public enum PageRotation
{
    /// <summary>
    /// No rotation
    /// </summary>
    Rotation0 = 0,

    /// <summary>
    /// Rotate 90 degrees
    /// </summary>
    Rotation90 = 90,

    /// <summary>
    /// Rotate 180 degrees
    /// </summary>
    Rotation180 = 180,

    /// <summary>
    /// Rotate 270 degrees
    /// </summary>
    Rotation270 = 270
}
